def read_rows(path: Path) -> list[dict[str, str]]:
    with path.open("r", encoding="utf-8-sig", newline="") as handle:
        return list(csv.DictReader(handle))


def normalize_english(term: str) -> str:
    normalized = unicodedata.normalize("NFKC", term)
    if all((ch.isascii() and (ch.isalnum() or ch in "- .'&/")) for ch in normalized) and any(
        ch.isalpha() for ch in normalized
    ):
        return normalized
    return ""


def suggestion(row: dict[str, str]) -> tuple[str, str]:
    term = row["term"]
    existing = row.get("existing_targets", "").strip()
    if existing and term not in OVERRIDE_EXISTING:
        return existing.split(" | ")[0], "沿用已有"
    if row["category"] == "latin_or_acronym":
        return MANUAL.get(term) or normalize_english(term), "保留英文"
    if term in MANUAL:
        return MANUAL[term], "建议"
    return "", "待定"


def write_csv(rows: list[dict[str, str]]) -> None:
    fields = list(rows[0].keys())
    for col in ["suggestion_status", "suggested_cn"]:
        if col in fields:
            fields.remove(col)
    insert_at = fields.index("count") + 1
    fields[insert_at:insert_at] = ["suggested_cn", "suggestion_status"]
    with OUTCSV.open("w", encoding="utf-8-sig", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields, lineterminator="\n")
        writer.writeheader()
        writer.writerows(rows)


def write_md(rows: list[dict[str, str]]) -> None:
    with OUTMD.open("w", encoding="utf-8", newline="\n") as handle:
        handle.write("# TDA00 专有名词候选汉化名草案\n\n")
        handle.write(
            "说明：`保留英文` 表示日文槽原本就是英文、拉丁字母或全角英文缩写，汉化时建议保留英文写法。\n\n"
        )
        for category in ORDER:
            part = [row for row in rows if row["category"] == category]
            if not part:
                continue
            handle.write(f"## {CATEGORY_NAMES.get(category, category)}（{len(part)}）\n\n")
            for row in part:
                cn = row["suggested_cn"] or "【待定】"
                existing = f"，已有：{row['existing_targets']}" if row.get("existing_targets") else ""
                handle.write(
                    f"- {row['term']} -> {cn}（{row['suggestion_status']}，{row['count']}次{existing}）\n"
                )
            handle.write("\n")


def main() -> int:
    rows = read_rows(INFILE)
    for row in rows:
        cn, status = suggestion(row)
        row["suggested_cn"] = cn
        row["suggestion_status"] = status
        if not row.get("confirmed_translation"):
            row["confirmed_translation"] = cn

    write_csv(rows)
    write_md(rows)
    print(f"rows={len(rows)} filled={sum(1 for row in rows if row['suggested_cn'])} pending={sum(1 for row in rows if not row['suggested_cn'])}")
    print(OUTCSV)
    print(OUTMD)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
